    private void btn_novoClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_novoClienteActionPerformed
        if (idUser != 0) {

            ProdutoController produtoController = new ProdutoController();
            Produto produto = produtoController.read(idUser);

            txt_cadastro_nome.setText("");
            txt_cadastro_preco.setText("");
            txt_cadastro_unidade.setText("");

            txt_cadastro_nome.setText(produto.getNome());
            txt_cadastro_preco.setText(Double.toString(produto.getPreco()));
            txt_cadastro_unidade.setText(produto.getUnidade());
            idUser = produto.getId();

            tbl_produtos.setEnabled(false);
            txt_cadastro_nome.setEnabled(true);
            txt_cadastro_preco.setEnabled(true);
            txt_cadastro_unidade.setEnabled(true);

            btn_cancelar.setEnabled(true);
            btn_salvar.setEnabled(true);
        }
    }//GEN-LAST:event_btn_novoClienteActionPerformed

