    public void readJtable() {
        DefaultTableModel modelo = (DefaultTableModel) tbl_produtos.getModel();

        modelo.setNumRows(0);
        ProdutoController produtos = new ProdutoController();

        for (Produto p : produtos.list()) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.precoString(),
                p.getUnidade()
            });
        }

    }

