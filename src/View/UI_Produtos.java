    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed

        Produto p = new Produto();
        ProdutoController produtoController = new ProdutoController();

        p.setNome(txt_cadastro_nome.getText());
        p.setPreco(Double.parseDouble(txt_cadastro_preco.getText()));
        p.setUnidade(txt_cadastro_unidade.getText());

        p.setId(idUser);

        if (idUser == 0) {
            produtoController.create(p);
            idUser = 0;
        } else {
            produtoController.update(p);

        }
        txt_cadastro_nome.setText("");
        txt_cadastro_preco.setText("");
        txt_cadastro_unidade.setText("");
        readJtable();

        tbl_produtos.setEnabled(true);
        txt_cadastro_nome.setEnabled(false);
        txt_cadastro_preco.setEnabled(false);
        txt_cadastro_unidade.setEnabled(false);

        btn_cancelar.setEnabled(false);
        btn_salvar.setEnabled(false);
    }//GEN-LAST:event_btn_salvarActionPerformed

